package com.example.app_store.models;

public class ReviewAdmin {
    private int id;
    private int product_id;
    private int user_id;
    private int rating;
    private String comment;
    private String user_name;
    private String admin_reply;

    public String getAdmin_reply() { return admin_reply; }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getProduct_id() {
        return product_id;
    }

    public void setProduct_id(int product_id) {
        this.product_id = product_id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public void setAdmin_reply(String admin_reply) {
        this.admin_reply = admin_reply;
    }
}
